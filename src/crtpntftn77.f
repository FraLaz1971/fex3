      PROGRAM CRTPNT
        IMPLICIT NONE
        INTEGER SEED,NPOINTS,I
        CHARACTER*16 FNAME
        DOUBLE PRECISION RANDOM
        PARAMETER(SEED = 86456)
        PRINT *,'ENTER THE NUMBER OF POINTS'
        READ *,NPOINTS
        PRINT *,'SELECT FILE NAME'
        READ *,FNAME
        CALL DATE_TIME_SEED@
        OPEN(UNIT=11,FILE=FNAME,STATUS='UNKNOWN')
        WRITE(11,'(I8)')NPOINTS
        DO 10,I=1,NPOINTS
            WRITE(11,'(F10.4)')REAL(RANDOM())*1000
10      CONTINUE
        CLOSE(UNIT=11,STATUS='KEEP')
      END
